# Використання умовних операцій
x = -15
result = "Positive" if x > 0 else ("Negative" if x < 0 else "Zero")
print(result)

# Оператори булевої логіки
y = 15
if 5 < y < 20:
    print("y знаходиться у середині діапазону")

# Складні умови (проектація)
age = 30
if 25 <= age <= 60:
    print("Цей вік підходить для активної праці!")

# Цикли

# Використання циклу for
fruits = ["апельсин", "манго", "ківі"]
for fruit in fruits:
    print(fruit)

# Використання циклу while
counter = 0
while counter < 3:
    print(counter)
    counter += 1
