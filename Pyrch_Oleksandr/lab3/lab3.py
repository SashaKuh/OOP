# Оператори if і else
num1 = 4
num2 = 12
if num1 > num2:
    print("Перше число більше")
else:
    print("Перше число менше або дорівнює")

# Цикл for
my_list = [1, 2, 3, 4, 5]
for item in my_list:
    if item == 5:
        print("Елемент присутній у списку")
        break
else:
    print("Елемент відсутній у списку")